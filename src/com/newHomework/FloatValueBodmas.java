public class FloatValueBodmas {
    public static void main(String[] args) {
      // int e,f,g
       // float f
        int e = 10;
        int f = 75;
        int g = 20;
        float d = 54;
        System.out.println(g/d*e);//20/53*10=0.370370
                                  //0.370370*10
                                   //3.7037036
        System.out.println(d/f);//54/75
                                //0.72
        System.out.println(e+f-g);//10+75-20
                                   // 85-20
                                   // 65


    }



}
